package cloudsimple.news.entity;

public class Tag {

    //标签ID
    private  int tagid;

    //标签名
    private String tagname;

    public Integer getTagid() {
        return tagid;
    }

    public void setTagid(int tagid) {
        this.tagid = tagid;
    }

    public String getTagname() {
        return tagname;
    }

    public void setTagname(String tagname) {
        this.tagname = tagname;
    }
}
